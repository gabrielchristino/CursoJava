    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Integer numero = (Integer) txtNumero.getValue();
        
        panelCalc.setVisible(true);
        
        Integer restoDivisao = numero % 2;
        lblResto.setText(String.format("%.2f", (double)restoDivisao));
        
        double aoCubo = Math.pow(numero, 3);
        lblAoCubo.setText(String.format("%.2f", aoCubo));
        
        double raizQuadrada = Math.sqrt(numero);
        lblRaizQuadrada.setText(String.format("%.2f", raizQuadrada));
        
        double raizCubica = Math.cbrt(numero);
        lblRaizCubica.setText(String.format("%.2f", raizCubica));
        
        Integer absoluto = Math.abs(numero);
        lblAbsoluto.setText(String.format("%.2f", (double)absoluto)); 
    }//GEN-LAST:event_btnCalcularActionPerformed

